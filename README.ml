This repo contains a set of tools used for my thesis

        -cartesian2LL.m: project a point on the unit sphere and
                returns its coordinate;
        -LL2Cartesian.m: does the converse of the previous one;
        -rotateLL.m: rotate a lat/long encoded image accordingly to
         the angles provided;
        -surf_features_avg: computes some statistics on the number of
                features found in set of images;
